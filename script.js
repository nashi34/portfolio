function setLanguage(language) {
  const dictionary = translations[language] || translations['pt-PT'];

  document.documentElement.lang = language;
  document.title = dictionary['meta.title'];

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll('[data-i18n-attr]').forEach((element) => {
    element.dataset.i18nAttr.split(',').forEach((pair) => {
      const [attribute, key] = pair.split(':').map((value) => value.trim());
      if (attribute && dictionary[key]) {
        element.setAttribute(attribute, dictionary[key]);
      }
    });
  });

  const toggle = document.querySelector('[data-lang-toggle]');
  toggle?.setAttribute('aria-label', dictionary['language.toggleAria']);
  localStorage.setItem('portfolio-language', language);
}

function setupLanguageToggle() {
  const toggle = document.querySelector('[data-lang-toggle]');
  if (!toggle) return;

  const savedLanguage = localStorage.getItem('portfolio-language');
  const initialLanguage = savedLanguage === 'en' ? 'en' : 'pt-PT';
  setLanguage(initialLanguage);

  toggle.addEventListener('click', () => {
    const nextLanguage = document.documentElement.lang === 'en' ? 'pt-PT' : 'en';
    setLanguage(nextLanguage);
  });
}

function setupMenu() {
  const menuToggle = document.querySelector('.menu-toggle');
  const navLinks = document.querySelector('.nav-links');
  if (!menuToggle || !navLinks) return;

  menuToggle.addEventListener('click', () => {
    const isOpen = navLinks.classList.toggle('active');
    menuToggle.setAttribute('aria-expanded', String(isOpen));
  });

  navLinks.querySelectorAll('a').forEach((link) => {
    link.addEventListener('click', () => {
      navLinks.classList.remove('active');
      menuToggle.setAttribute('aria-expanded', 'false');
    });
  });
}

function setupActiveNav() {
  const navLinks = Array.from(document.querySelectorAll('.nav-links a[href^="#"]'));
  const sections = navLinks
    .map((link) => document.querySelector(link.getAttribute('href')))
    .filter(Boolean);

  if (!navLinks.length || !sections.length) return;

  const setActiveLink = (id) => {
    navLinks.forEach((link) => {
      const isActive = link.getAttribute('href') === `#${id}`;
      link.classList.toggle('is-active', isActive);
      if (isActive) {
        link.setAttribute('aria-current', 'page');
      } else {
        link.removeAttribute('aria-current');
      }
    });
  };

  let isNavClickScrolling = false;
  let scrollEndTimer;

  const updateActiveLink = () => {
    if (isNavClickScrolling) return;

    const headerHeight = document.querySelector('.site-header')?.offsetHeight || 0;
    const scrollPosition = window.scrollY + headerHeight + 24;
    const pageBottom = window.scrollY + window.innerHeight >= document.documentElement.scrollHeight - 4;

    if (pageBottom) {
      setActiveLink(sections[sections.length - 1].id);
      return;
    }

    const activeSection = sections.reduce((current, section) => {
      return section.offsetTop <= scrollPosition ? section : current;
    }, sections[0]);

    setActiveLink(activeSection.id);
  };

  updateActiveLink();
  navLinks.forEach((link) => {
    link.addEventListener('click', () => {
      const target = document.querySelector(link.getAttribute('href'));
      if (target) {
        isNavClickScrolling = true;
        setActiveLink(target.id);
      }
    });
  });

  window.addEventListener('scroll', () => {
    if (isNavClickScrolling) {
      clearTimeout(scrollEndTimer);
      scrollEndTimer = setTimeout(() => {
        isNavClickScrolling = false;
        updateActiveLink();
      }, 140);
      return;
    }

    updateActiveLink();
  }, { passive: true });
  window.addEventListener('resize', updateActiveLink);
}

function setupRevealAnimations() {
  const items = document.querySelectorAll('.reveal, .project-card, .skill-card');

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('is-visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.14 });

  items.forEach((item) => {
    item.classList.add('reveal');
    observer.observe(item);
  });
}

function setupImageModal() {
  document.querySelectorAll('button.image-button img').forEach((img) => {
    img.closest('button').addEventListener('click', () => {
      const modal = document.createElement('div');
      modal.className = 'modal';
      modal.innerHTML = `
        <button class="close" type="button" aria-label="Fechar">&times;</button>
        <img class="modal-content" src="${img.src}" alt="${img.alt}">
      `;

      document.body.appendChild(modal);

      const closeModal = () => modal.remove();
      modal.querySelector('.close').addEventListener('click', closeModal);
      modal.addEventListener('click', (event) => {
        if (event.target === modal) closeModal();
      });
      document.addEventListener('keydown', function closeOnEscape(event) {
        if (event.key === 'Escape') {
          closeModal();
          document.removeEventListener('keydown', closeOnEscape);
        }
      });
    });
  });
}

function setupVideos() {
  const videos = document.querySelectorAll('.gallery-video');

  videos.forEach((video) => {
    const container = video.closest('.video-container');
    const playButton = container?.querySelector('.video-play');

    video.volume = 0.1;

    const playVideo = () => {
      video.muted = false;
      video.volume = 0.1;
      if (video.paused) {
        video.play();
      }
    };

    playButton?.addEventListener('click', (event) => {
      event.stopPropagation();
      playVideo();
    });

    video.addEventListener('click', playVideo);

    video.addEventListener('play', () => {
      video.muted = false;
      video.volume = 0.1;
      container?.classList.add('is-playing');

      videos.forEach((otherVideo) => {
        if (otherVideo !== video && !otherVideo.paused) {
          otherVideo.pause();
        }
      });
    });

    video.addEventListener('pause', () => {
      container?.classList.remove('is-playing');
    });

    video.addEventListener('ended', () => {
      container?.classList.remove('is-playing');
    });
  });
}

function setupSkillFilters() {
  const buttons = document.querySelectorAll('.category-btn');
  const cards = document.querySelectorAll('.skill-card');
  if (!buttons.length || !cards.length) return;

  buttons.forEach((btn) => {
    btn.addEventListener('click', () => {
      buttons.forEach((b) => b.classList.remove('active'));
      btn.classList.add('active');

      const filter = btn.dataset.filter;

      cards.forEach((card) => {
        const categories = card.dataset.category ? card.dataset.category.split(' ') : [];
        if (filter === 'all' || categories.includes(filter)) {
          card.classList.remove('hidden');
        } else {
          card.classList.add('hidden');
        }
      });
    });
  });
}

function setupProjectTilt() {
  if (window.matchMedia('(hover: hover)').matches) {
    const cards = document.querySelectorAll('.project-card');

    cards.forEach((card) => {
      card.addEventListener('mousemove', (e) => {
        const rect = card.getBoundingClientRect();
        const x = e.clientX - rect.left;
        const y = e.clientY - rect.top;

        const width = rect.width;
        const height = rect.height;

        const percentX = (x / width) - 0.5;
        const percentY = (y / height) - 0.5;

        const maxTiltX = 8;
        const maxTiltY = 8;

        const rotateX = -(percentY * maxTiltX).toFixed(2);
        const rotateY = (percentX * maxTiltY).toFixed(2);

        card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) scale3d(1.015, 1.015, 1.015)`;
        card.style.transition = 'transform 80ms ease';
      });

      card.addEventListener('mouseleave', () => {
        card.style.transform = 'perspective(1000px) rotateX(0deg) rotateY(0deg) scale3d(1, 1, 1)';
        card.style.transition = 'transform 450ms cubic-bezier(0.16, 1, 0.3, 1)';
      });
    });
  }
}

document.addEventListener('DOMContentLoaded', () => {
  setupLanguageToggle();
  setupMenu();
  setupActiveNav();
  setupRevealAnimations();
  setupImageModal();
  setupVideos();
  setupSkillFilters();
  setupProjectTilt();
});
